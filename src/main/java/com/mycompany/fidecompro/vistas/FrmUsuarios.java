    private void btnGuardarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarUsuarioActionPerformed
        String nombre = txtNombreCompleto.getText().trim();
        String nombreUsuario = txtNombreUsuario.getText().trim();
        String contrasena = String.valueOf(txtContrasena.getPassword());
        String rol = cmbRol.getSelectedItem().toString();
        if(nombre.isEmpty()||nombreUsuario.isEmpty()||contrasena.isEmpty()||cmbRol.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(this,"Debe completar todos los campos.","Datos incompletos.", JOptionPane.WARNING_MESSAGE);
            return;        
        }
        try{
            int nuevoID = gestorUsuarios.obtenerUsuarios().size()+1;
            Usuario nuevoUsuario = new Usuario(nuevoID,nombre,nombreUsuario,contrasena,rol);
        
        gestorUsuarios.agregarUsuario(nuevoUsuario);
        cargarTablaUsuarios();
        limpiarCampos();
        JOptionPane.showMessageDialog(this,"Usuario registrado correctamente.","Registro exitoso",JOptionPane.INFORMATION_MESSAGE);
        } catch (UsuarioDuplicadoException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(),"Usuario duplicado", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarUsuarioActionPerformed

