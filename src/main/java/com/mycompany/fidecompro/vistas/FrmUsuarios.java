    public void cargarTablaUsuarios(){
        DefaultTableModel modelo =(DefaultTableModel) tblUsuarios.getModel();
        modelo.setRowCount(0);
        

        for (Usuario usuario : gestorUsuarios.obtenerUsuarios()) { Object[] fila = { usuario.getId(),usuario.getNombre(),usuario.getUsuario(),usuario.getRol()
    };
        
    modelo.addRow(fila);
        }
    }    
