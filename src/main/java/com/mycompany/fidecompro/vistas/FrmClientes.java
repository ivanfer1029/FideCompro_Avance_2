    private void cargarTablaClientes() {

    DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();

    modelo.setRowCount(0);

    for (Cliente cliente : gestorClientes.obtenerClientes()) {

        Object[] fila = {
            cliente.getId(),
            cliente.getNombre(),
            cliente.getCedula(),
            cliente.getTelefono(),
            cliente.getCorreo(),
            cliente.getDireccion()
        };

        modelo.addRow(fila);
    }
}
    private void cargarTablaCliente(){
        DefaultTableModel modelo = (DefaultTableModel)tblClientes.getModel();
        modelo.setRowCount(0);
        for(Cliente cliente : gestorClientes.obtenerClientes()){
            Object[] fila = { cliente.getId(),cliente.getNombre(),cliente.getCedula(),cliente.getTelefono(),cliente.getCorreo(),cliente.getDireccion()};
            modelo.addRow(fila);
        }
    }
    
    private void mostrarSiguienteId() {

    int siguienteId =
            gestorClientes.obtenerClientes().size() + 1;

    txtIdCliente.setText(
            String.valueOf(siguienteId)
    );
}
