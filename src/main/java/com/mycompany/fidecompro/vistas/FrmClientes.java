    private void btnBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarClienteActionPerformed
        
        String cedulaBuscada = txtBuscarCliente.getText().trim();

        if (cedulaBuscada.isEmpty()) {JOptionPane.showMessageDialog(this,"Ingrese la cédula que desea buscar.", "Búsqueda",JOptionPane.WARNING_MESSAGE);

            return;
        
        }

        try {

            Cliente clienteEncontrado =gestorClientes.buscarCliente(cedulaBuscada);
            

            txtIdCliente.setText( String.valueOf(clienteEncontrado.getId()));

            txtNombreCompleto.setText( clienteEncontrado.getNombre() );

            txtCedula.setText(clienteEncontrado.getCedula());

            txtTelefono.setText(clienteEncontrado.getTelefono() );

            txtCorreo.setText(clienteEncontrado.getCorreo());

            txtDireccion.setText(clienteEncontrado.getDireccion() );

        } catch (ClienteNoEncontradoException e) {

            JOptionPane.showMessageDialog( this,e.getMessage(), "Cliente no encontrado",JOptionPane.ERROR_MESSAGE );
        }   
    }//GEN-LAST:event_btnBuscarClienteActionPerformed

