    private void btnGuardarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarClienteActionPerformed
        String nombre = txtNombreCompleto.getText().trim();
        String cedula = txtCedula.getText().trim();
        String telefono =  txtTelefono.getText().trim();
        String correo = txtCorreo.getText().trim();
        String direccion = txtDireccion.getText().trim();

        if (nombre.isEmpty() || cedula.isEmpty()|| telefono.isEmpty()|| correo.isEmpty()|| direccion.isEmpty()) {JOptionPane.showMessageDialog(this,"Debe completar todos los campos.","Datos incompletos",JOptionPane.WARNING_MESSAGE);
        return;
        } try {
            int nuevoId = gestorClientes.obtenerClientes().size() + 1;
            Cliente nuevoCliente = new Cliente(nuevoId,nombre,cedula,telefono, correo, direccion);

            gestorClientes.registrarCliente(nuevoCliente);
            cargarTablaClientes();
            limpiarCampos();

            JOptionPane.showMessageDialog(this,"Cliente registrado correctamente.","Registro exitoso",JOptionPane.INFORMATION_MESSAGE);

             } catch (ClienteDuplicadoExcepcion e) {JOptionPane.showMessageDialog(this,e.getMessage(),"Cliente duplicado", JOptionPane.ERROR_MESSAGE );
        }
    }//GEN-LAST:event_btnGuardarClienteActionPerformed

