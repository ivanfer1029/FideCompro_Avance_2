    private void txtBuscarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBuscarProductoActionPerformed
        String codigoTexto =
            txtBuscarProducto.getText().trim();

    if (codigoTexto.isEmpty()) {

        JOptionPane.showMessageDialog(
                this,
                "Ingrese el código que desea buscar.",
                "Búsqueda",
                JOptionPane.WARNING_MESSAGE
        );

        return;
    }

    try {

        int codigo =
                Integer.parseInt(codigoTexto);

        Producto productoEncontrado =
                gestorProductos.buscarProducto(codigo);

        txtCodigoProducto.setText(
                String.valueOf(productoEncontrado.getIdProducto())
        );

        txtNombreProducto.setText(
                productoEncontrado.getNombre()
        );

        cmbCategoria.setSelectedItem(
                productoEncontrado.getCategoria()
        );

        txtPrecio.setText(
                String.valueOf(productoEncontrado.getPrecio())
        );

        txtCantidadDisponible.setText(
                String.valueOf(
                        productoEncontrado.getCantidadDisponible()
                )
        );

        txtDescripcion.setText(
                productoEncontrado.getDescripcion()
        );

    } catch (NumberFormatException e) {

        JOptionPane.showMessageDialog(
                this,
                "El código debe ser un número entero.",
                "Formato incorrecto",
                JOptionPane.ERROR_MESSAGE
        );

    } catch (ProductoNoEncontradoException e) {

        JOptionPane.showMessageDialog(
                this,
                e.getMessage(),
                "Producto no encontrado",
                JOptionPane.ERROR_MESSAGE
        );
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_txtBuscarProductoActionPerformed

