    private void btnGuardarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarProductoActionPerformed
         String codigoTexto =txtCodigoProducto.getText().trim();
         String nombre =txtNombreProducto.getText().trim();
         String precioTexto =txtPrecio.getText().trim();
         String cantidadTexto =txtCantidadDisponible.getText().trim();
         String descripcion =txtDescripcion.getText().trim();
         if (codigoTexto.isEmpty()|| nombre.isEmpty()|| precioTexto.isEmpty()|| cantidadTexto.isEmpty() || descripcion.isEmpty() || cmbCategoria.getSelectedIndex() == 0) {
             JOptionPane.showMessageDialog(this,"Debe completar todos los campos.","Datos incompletos", JOptionPane.WARNING_MESSAGE);
             

             return;
            }
        try {
            int codigo = Integer.parseInt(codigoTexto);
            double precio = Double.parseDouble(precioTexto);
            int cantidad = Integer.parseInt(cantidadTexto);
            if (codigo <= 0 || precio <= 0 || cantidad < 0) {
                JOptionPane.showMessageDialog(this,"El código y el precio deben ser mayores que cero, " + "y la cantidad no puede ser negativa.","Valores inválidos",JOptionPane.WARNING_MESSAGE);               
                return;
            }

            String categoria = cmbCategoria.getSelectedItem().toString();

            Producto nuevoProducto = new Producto(codigo,nombre,categoria, precio,cantidad, descripcion );

            gestorProductos.registrarProducto(nuevoProducto);
            cargarTablaProductos();
            limpiarCampos();
            JOptionPane.showMessageDialog(this, "Producto registrado correctamente.", "Registro exitoso",JOptionPane.INFORMATION_MESSAGE);
            
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this,"El código y la cantidad deben ser números enteros, "+ "y el precio debe ser numérico.","Formato incorrecto",JOptionPane.ERROR_MESSAGE);
                

            } catch (ProductoDuplicadoException e) {
                JOptionPane.showMessageDialog(this,e.getMessage(),"Producto duplicado",JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnGuardarProductoActionPerformed

